# Operacoes aritmeticas entre dois numeros


#Entrada do usuario
a = int(input())
b = int(input())

#Exibicao dos valores de entrada
print('a = ' + str(a))
print('b = ' + str(b))

#Operacoes aritmeticas
print('a + b = {}'.format(a + b))    # Soma
print('a - b = {}'.format(a - b))    # Subtracao
print('a * b = {}'.format(a * b))    # Multiplicacao
print('a // b = {}'.format(a // b))  # Divisao inteira
print('a % b = {}'.format(a % b))    # Resto da divisao
